export function makeEven(i) {
    //dubles number so to get the right group
    if (i == 0 || i >= 2) {
        return i * 2;
    } else {
        return i + 1;
    }
}

export function reverseEven(i) {

    if (i == 0 || i >= 2) {
        return i / 2;
    } else {
        return i - 1;
    }

}






